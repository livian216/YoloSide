    def _log_context_menu(self, pos):
        """Right-click context menu for the log panel."""
        menu = QMenu(self.log_text)
        clear_action = menu.addAction("Clear Log")
        copy_action = menu.addAction("Copy All")
        action = menu.exec(self.log_text.mapToGlobal(pos))
        if action == clear_action:
            self.log_text.clear()
        elif action == copy_action:
            from PySide6.QtWidgets import QApplication
            QApplication.clipboard().setText(self.log_text.toPlainText())
            self.show_status("Log copied to clipboard")

    def _log(self, msg: str):
        """Append a timestamped message to the log panel."""
        if not hasattr(self, 'log_text'):
            return
        from datetime import datetime
        ts = datetime.now().strftime("%H:%M:%S")
        self.log_text.append(f"[{ts}] {msg}")
        # Auto-scroll to bottom
        from PySide6.QtWidgets import QScrollBar
        vbar = self.log_text.verticalScrollBar()
        if vbar:
            vbar.setValue(vbar.maximum())

    def _select_batch_folder(self):
        """Open folder picker and store path for later batch processing."""
        folder = QFileDialog.getExistingDirectory(
            self, "Select Image Folder", os.getcwd())
        if not folder:
            return
        self._batch_folder = folder
        # Show brief folder name in the batch section
        disp = os.path.basename(folder)
        if len(disp) > 22:
            disp = disp[:20] + "..."
        self.batch_path_label.setText(f"📁 {disp}")
