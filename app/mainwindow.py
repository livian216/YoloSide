    # ── Config ────────────────────────────────────────────────────

    def load_config(self):
        config_file = _writable_path('config/setting.json')
        if not os.path.exists(config_file):
            iou, conf, rate = 0.26, 0.33, 10
            save_res, save_txt = 0, 0
            with open(config_file, 'w', encoding='utf-8') as f:
                json.dump({
                    "iou": iou, "conf": conf, "rate": rate,
                    "save_res": save_res, "save_txt": save_txt
                }, f, ensure_ascii=False, indent=2)
        else:
            config = json.load(open(config_file, 'r', encoding='utf-8'))
            if len(config) != 5:
                iou, conf, rate = 0.26, 0.33, 10
                save_res, save_txt = 0, 0
            else:
                iou = config['iou']
                conf = config['conf']
                rate = config['rate']
                save_res = config['save_res']
                save_txt = config['save_txt']

        self.yolo_engine.iou_thres = iou
        self.yolo_engine.conf_thres = conf
        self.yolo_engine.speed_thres = rate
        self.yolo_engine.save_res = (save_res != 0)
        self.yolo_engine.save_txt = (save_txt != 0)

        self.iou_spinbox.setValue(iou)
        self.iou_slider.setValue(int(iou * 100))
        self.conf_spinbox.setValue(conf)
        self.conf_slider.setValue(int(conf * 100))
        self.speed_spinbox.setValue(rate)
        self.speed_slider.setValue(rate)
        self.save_res_button.setCheckState(Qt.CheckState(save_res))
        self.save_txt_button.setCheckState(Qt.CheckState(save_txt))
        self.run_button.setChecked(False)
        self.show_status("Welcome~")

    # ── Stop ──────────────────────────────────────────────────────

    def stop(self):
        # Always call engine.stop() — it's safe even when idle
        # (all internals check for None) and it clears _source.
        # Previously the _inference_active guard skipped it when
        # inference hadn't started, leaking the source selection.
        self.yolo_engine.stop()
        self._inference_active = False
        self.run_button.setChecked(False)
        self.save_res_button.setEnabled(True)
        self.save_txt_button.setEnabled(True)
        self.pre_video.clear()
        self.res_video.clear()
        self.progress_bar.setValue(0)
        self.Class_num.setText('--')
        self.Target_num.setText('--')
        self.fps_label.setText('--')
        self.show_status("Stopped.")

    # ── Parameter changes ─────────────────────────────────────────

    def change_val(self, x, flag):
        if flag == 'iou_spinbox':
            self.iou_slider.setValue(int(x * 100))
        elif flag == 'iou_slider':
            self.iou_spinbox.setValue(x / 100)
            self.yolo_engine.iou_thres = x / 100
            self.show_status(f'IOU Threshold: {x / 100:.2f}')
        elif flag == 'conf_spinbox':
            self.conf_slider.setValue(int(x * 100))
        elif flag == 'conf_slider':
            self.conf_spinbox.setValue(x / 100)
            self.yolo_engine.conf_thres = x / 100
            self.show_status(f'Conf Threshold: {x / 100:.2f}')
        elif flag == 'speed_spinbox':
            self.speed_slider.setValue(x)
        elif flag == 'speed_slider':
            self.speed_spinbox.setValue(x)
            self.yolo_engine.speed_thres = x
            self.show_status(f'Delay: {x} ms')

    # ── Model management ──────────────────────────────────────────

    def on_model_changed(self, name: str, task_type=None):
        if name:
            if task_type is None:
                task_type = self._current_task_type
            # Strip 🌐 prefix for downloadable models
            clean_name = name.replace("🌐 ", "")
            # Use writable path so ultralytics can persist downloaded models
            model_path = f"{_writable_path('models')}/{clean_name}" if "/" not in clean_name else clean_name
            self.yolo_engine.set_model(model_path)
            self.Model_name.setText(clean_name)
            self.show_status(f'Model: {clean_name}')
            # Remember user's choice for this task type
            self._task_model_selections[task_type] = clean_name

    def refresh_model_list(self):
        """Populate model_box based on current task type."""
        self._update_model_list_for_task(self._current_task_type)

    def ModelBoxRefre(self):
        """Periodic check for new/deleted model files."""
        # Don't refresh while the user is browsing the dropdown
        if self.model_box.view().isVisible():
            return
        models_dir = _writable_path('models')
        if not os.path.isdir(models_dir):
            return
        current_all = sorted(
            [f for f in os.listdir(models_dir) if f.endswith('.pt')],
            key=lambda x: os.path.getsize(os.path.join(models_dir, x)))
        if current_all != self.pt_list:
            self.refresh_model_list()

