    # ── Run / Pause ───────────────────────────────────────────────

    def run_or_continue(self):
        # Check for batch folder first — batch mode overrides normal inference
        batch_folder = getattr(self, '_batch_folder', None)
        if batch_folder and os.path.isdir(batch_folder):
            self._run_batch(batch_folder)
            return

        source = self.yolo_engine._source if self.yolo_engine._source else ''
        if not source:
            self.show_status(
                'Please select a video source before starting detection...')
            self.run_button.setChecked(False)
        else:
            if self.run_button.isChecked():
                if self._inference_active:
                    # Already running — just unpause
                    self.yolo_engine.pause(False)
                    self.show_status('Detecting...')
                else:
                    # Start fresh inference
                    self.run_button.setChecked(True)
                    self.save_txt_button.setEnabled(False)
                    self.save_res_button.setEnabled(False)
                    self.show_status('Detecting...')
                    # The worker thread stays alive across runs
                    # (we never quit() it — just stop the engine).
                    if not self.yolo_thread.isRunning():
                        self.yolo_thread.start()
                    self._inference_active = True
                    self.main2yolo_begin_sgl.emit()
            else:
                self.yolo_engine.pause(True)
                self.show_status("Pause...")
                self.run_button.setChecked(False)

