# ── Path helpers for PyInstaller compatibility ────────────────────

def _resource_path(relative_path: str) -> str:
    """Get absolute path to a read-only resource file.
    Works in both dev mode and PyInstaller bundled mode.
    Project root = parent of the app/ directory (since mainwindow.py lives in app/)."""
    if getattr(sys, 'frozen', False):
        base = sys._MEIPASS
    else:
        # mainwindow.py is in app/ → go up one level to project root
        base = os.path.dirname(os.path.dirname(os.path.abspath(__file__)))
    return os.path.join(base, relative_path)


def _writable_path(relative_path: str) -> str:
    """Get absolute path for writable files (config, output).
    In bundled mode, writes next to the exe, not inside _MEIPASS.
    Creates parent directories as needed.
    Project root = parent of the app/ directory (since mainwindow.py lives in app/)."""
    if getattr(sys, 'frozen', False):
        base = os.path.dirname(sys.executable)
    else:
        # mainwindow.py is in app/ → go up one level to project root
        base = os.path.dirname(os.path.dirname(os.path.abspath(__file__)))
    full = os.path.join(base, relative_path)
    os.makedirs(os.path.dirname(full), exist_ok=True)
    return full


