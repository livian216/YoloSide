    def closeEvent(self, event):
        config = {
            'iou': self.iou_spinbox.value(),
            'conf': self.conf_spinbox.value(),
            'rate': self.speed_spinbox.value(),
            'save_res': 0 if self.save_res_button.checkState() == Qt.Unchecked else 2,
            'save_txt': 0 if self.save_txt_button.checkState() == Qt.Unchecked else 2,
        }
        try:
            config_file = _writable_path('config/setting.json')
            with open(config_file, 'w', encoding='utf-8') as f:
                json.dump(config, f, ensure_ascii=False, indent=2)
        except Exception:
            pass
        if self._batch_thread and self._batch_thread.isRunning():
            self._batch_processor.stop()
            self._batch_thread.quit()
            self._batch_thread.wait(3000)
        if self._inference_active:
            self.yolo_engine.stop()
        if self.yolo_thread.isRunning():
            self.yolo_thread.quit()
            self.yolo_thread.wait(3000)
        sys.exit(0)
