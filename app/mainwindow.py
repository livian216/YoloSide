    def on_task_changed(self, index: int):
        """Handle task type selection change."""
        display_names = get_task_display_names()
        if 0 <= index < len(display_names):
            task_type = task_type_from_display_name(display_names[index])
            self._current_task_type = task_type
            task_config = TASK_REGISTRY[task_type]

            # Update title
            self.char_label.setText(task_config.result_label)

            # Update engine
            self.yolo_engine.set_task(task_type)

            # Update model list for this task
            self._update_model_list_for_task(task_type)

            self.show_status(f"Task: {task_config.display_name}")

    def _update_model_list_for_task(self, task_type: TaskType):
        """Populate model_box with locally available models for the given task."""
        task_config = TASK_REGISTRY[task_type]
        registry_models = set(task_config.available_models)

        # Get locally downloaded models that match this task.
        # Use _writable_path so models survive app restarts
        # (in bundled mode, _MEIPASS is a temp dir that gets wiped).
        models_dir = _writable_path('models')
        local_models = []
        if os.path.isdir(models_dir):
            for f in os.listdir(models_dir):
                if f.endswith('.pt'):
                    local_models.append(f)

        # Show: local models that are in the registry for this task
        # + any local models not in registry (custom models)
        local_registry = [m for m in local_models if m in registry_models]
        local_registry.sort(key=lambda x: os.path.getsize(os.path.join(models_dir, x))
                           if os.path.exists(os.path.join(models_dir, x)) else 0)

        # Add marker for downloadable models
        available = list(local_registry)
        # Also add models that can be downloaded (marked with 🌐)
        for m in task_config.available_models:
            if m not in local_models:
                available.append(f"🌐 {m}")

        current = self.model_box.currentText()
        # Strip 🌐 prefix for comparison
        current_clean = current.replace("🌐 ", "") if current else ""

        # Block signals during rebuild — addItems() would otherwise fire
        # currentTextChanged → on_model_changed → overwrite _task_model_selections
        # BEFORE our selection logic below gets a chance to run.
        self.model_box.blockSignals(True)
        self.model_box.clear()
        self.model_box.addItems(available)

        # Restore selection priority:
        # 1) User's previously saved choice for this task type
        # 2) Current selection if still in the list
        # 3) First available local model (if any)
        # 4) Leave empty (user must pick)
        saved = self._task_model_selections.get(task_type, None)
        if saved and saved in local_registry:
            self.model_box.setCurrentText(saved)
        elif saved and saved in task_config.available_models:
            # Model not downloaded yet — select it with 🌐 prefix
            self.model_box.setCurrentText(f"🌐 {saved}")
        elif current_clean in local_registry:
            self.model_box.setCurrentText(current_clean)
        elif local_registry:
            self.model_box.setCurrentText(local_registry[0])

        self.model_box.blockSignals(False)
        # Apply the final selection — use the passed task_type
        # (not self._current_task_type, which may be stale)
        final = self.model_box.currentText()
        final_clean = final.replace("🌐 ", "") if final else ""
        # Only call on_model_changed if the model actually DIFFERS from
        # what the engine already has.  This prevents the 2-second
        # ModelBoxRefre timer from nuking _model during active inference.
        # _model_name is a full path (e.g. D:\...\models\yolov8n.pt),
        # final_clean is just the filename — compare basenames.
        engine_basename = os.path.basename(self.yolo_engine._model_name) if self.yolo_engine._model_name else ""
        if final and final_clean != engine_basename:
            self.on_model_changed(final, task_type=task_type)

        self.pt_list = sorted(local_models)  # Update cached list (sorted for ModelBoxRefre compare)

